#include<stdio.h>

int main() {
    int sum = 1;
    for (int i = 3; i <= 1001; i += 2) { // 第 n(n != 1) 层对角线和为 4 * n * n - 6 * n + 6
        sum += 4 * i * i - 6 * i + 6;
    }
    printf("%d\n", sum);
    return 0;
}
